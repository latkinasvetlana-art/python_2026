# 1. Объединение данных в строку
# Напишите функцию, которая принимает список любых данных (строки, числа, списки, словари) и возвращает их строковое представление, объединённое через " | ". Добавьте документацию и аннотации типов для всех параметров и возвращаемого значения.

# Данные:
# data = [42, "hello", [1, 2, 3], {"a": 1, "b": 2}]
# Пример вывода:
# 42 | hello | [1, 2, 3] | {'a': 1, 'b': 2}

# Импортируем тип Any (означает "любой тип данных")
from typing import Any


# Определяем функцию
# items — это список, в котором могут быть любые типы (числа, строки, списки, словари)
# -> str означает, что функция вернёт строку
def join_as_string(items: list[Any]) -> str:
    
    # map(str, items):
    # берёт каждый элемент из списка items
    # и превращает его в строку
    
    # " | ".join(...):
    # соединяет все строки в одну
    # между ними вставляет " | "
    
    # return — возвращает результат работы функции
    return " | ".join(map(str, items))


# Исходные данные — список с разными типами
data = [42, "hello", [1, 2, 3], {"a": 1, "b": 2}]


# Вызываем функцию и выводим результат на экран
print(join_as_string(data))


# Ожидаемый результат:
# 42 | hello | [1, 2, 3] | {'a': 1, 'b': 2}